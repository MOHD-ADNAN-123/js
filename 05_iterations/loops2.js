//************WHILE **********/

let i=0;
while(i<5){
    console.log(i);
    i=i+1
}

//**** ARRAY 
let myArr =["flash","batman","superman"]
let index =0;
while (index<myArr.length) {
    console.log(myArr[index]);
    index=index+1
}


//************DO WHILE LOOP********** */

// FIRST THE WORK IS DONE THEN THE CONDITION WILL CHECK
let a=1
do{
    console.log(a);
    a++;
}
while(a<5);

